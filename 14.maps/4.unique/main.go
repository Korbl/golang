package main

import (
	"fmt"
)

func main() {
	fmt.Println("Unique elements: ", UniqueElements([]string{"1", "2", "2", "3", "4", "4"}))
}

// Implement the unique elements function which returns
// the amount of unique elements in a given string slice.
// Use a map to get the result.
// Run the tests to verify your result.
func UniqueElements(list []string) int {
	uniqueCount := map[string]int{}
	for _, v := range list {
		uniqueCount[v] = 1
	}
	return len(uniqueCount)
}
